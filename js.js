function about() {
    let HereMe = document.querySelectorAll(".HereMe")
    let card = document.querySelectorAll(".card")
    for (let i = 0; i < HereMe.length; i++) {
        HereMe[i].addEventListener("click", () => {
            card[i].classList.toggle("flip")
        })
    }
    let close=document.querySelectorAll(".close")
    for(let i=0;i<close.length;i++){
        close[i].addEventListener("click",()=>{
            card[i].classList.remove("flip")
        })
    }
}

function opecoa(array) {
    let ope2 = document.getElementById("ope")
    ope2.addEventListener("change", () => {
        ope2 = document.getElementById("ope").value
        switch (ope2) {
            case "1":
                clean()
                let novo1 = array.filter(item => { return item.type == "Acção" })
                clear()
                if (novo1.length > 0) {
                    forCard(novo1)
                    about()
                }
                else {
                    conent.appendChild(p)
                }
                break
            case "2":
                clean()
                let novo2 = array.filter(item => { return item.type == "Terror" })
                clear()
                if (novo2.length > 0) {
                    forCard(novo2)
                    about()
                } else {
                    conent.appendChild(p)
                }
                break
            case "3":
                clean()
                let novo3 = array.filter(item => { return item.type == "Romance" })
                clear()
                if (novo3.length > 0) {
                    forCard(novo3)
                    about()
                }
                else {
                    conent.appendChild(p)
                }
                break
            case "4":
                clean()
                let novo4 = array.filter(item => { return item.type == "Aventura" })
                clear()
                if (novo4 > 0) {
                    forCard(novo4)
                    about()
                } else {
                    conent.appendChild(p)
                }
                break
            default:
                clean()
                clear()
                forCard(array)
                about()
                break
        }
    })
}
function clean() {
    if (conent.contains(p)) {
        conent.removeChild(p)
    }
}
function clear() {
    let clear = document.querySelectorAll(".card")
    for (let i = 0; i < clear.length; i++) {
        clear[i].remove()
    }
}
